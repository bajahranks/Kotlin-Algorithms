object TwoByTwoLinearEquations {
    @JvmStatic
    fun main(args: Array<String>) {
        val solveX = (44.5 * 55 - 50.2 * 5.9) / (3.4 * 55 - 50.2 * 2.1)
        val solveY = (3.4 * 5.9 - 44.5 * 2.1) / (3.4 * 55 - 50.2 * 2.1)

        println("X is: $solveX")
        println("Y is: $solveY")
    }
}
