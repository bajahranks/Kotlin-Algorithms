object DisplayJavaPattern {
    @JvmStatic
    fun main(args: Array<String>) {
        println("    J" + "   A   " + "V     V" + "   A   ")
        println("    J" + "  A A  " + " V   V " + "  A A  ")
        println("J   J" + " AAAAA " + "  V V  " + " AAAAA ")
        println(" J J " + "A     A" + "   V   " + "A     A")
    }
}